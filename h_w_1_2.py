a = float(input('Введите первое число >> '))
b = float(input('Введите второе число >> '))
print('Сума ', a,'+',b,'=',a+b)
print('Разница ', a,'-',b,'=',a-b)
print('Умножение ', a,'*',b,'=',a*b)
print('Деление ', a,'/',b,'=',a/b)
print('Деление без остатка ', a,'//',b,'=',a//b)
print('Остаток от деления ', a,'%',b,'=',a%b)
print('Степень ', a,'**',b,'=',a**b)
print('Сравнение ', a, '>',b,'>>',a>b)
print('Сравнение ', a, '<',b,'>>',a<b)
print('Сравнение ', a, '>=',b,'>>',a>=b)
print('Сравнение ', a, '<=',b,'>>',a<=b)
print('Сравнение ', a, '==',b,'>>',a==b)
print('Сравнение ', a, '!=',b,'>>',a!=b)
print('Логическое и ', a, 'and',b,'>>',a and b)
print('Логическое или ', a, 'or',b,'>>',a or b)
